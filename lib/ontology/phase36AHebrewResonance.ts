const uniqueAliases = (values: string[]) => {
  const seen = new Set<string>();

  return values.filter((value) => {
    const normalized = value.toLocaleLowerCase("de");

    if (seen.has(normalized)) return false;
    seen.add(normalized);

    return true;
  });
};

